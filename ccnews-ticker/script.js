function random(list) {
	return list[random_int(0, list.length - 1)];
}

function random_int(min, max) {
	min = Math.ceil(min);
	max = Math.floor(max);
	return Math.floor(Math.random() * (max - min + 1)) + min;
}

function rand_int_string_input(string) {
	const match = string.match(/\[(\d+)\|(\d+)\]/);
	if (match) {
		const min = parseInt(match[1]);
		const max = parseInt(match[2]);
		return string.replace(match[0], random_int(min, max));
	}
	return string;
}

function random_string_input(string) {
	return string.replace(/\[([^\[\]]+\/[^\[\]]+)\]/g, (match, contents) => {
		const options = contents.split('/');
		return random(options);
	});
}

function generate_news() {
	let article = (random_int(0, 1000) === 777) ? random(news.lucky) : random(news.normal);
	article = rand_int_string_input(article);

	for (const word in wordList) {
		if (wordList.hasOwnProperty(word)) {
			const regex = new RegExp(`\\[${word}\\]`, "g");
			article = article.replace(regex, random(wordList[word]));
		}
	}
	return article;
}

function on_click() {
	document.getElementById("news").innerText = generate_news();
}

